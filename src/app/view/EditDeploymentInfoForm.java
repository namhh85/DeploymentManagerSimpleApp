    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String description = jTextArea1.getText();
        Integer status;
        if (jRadioButton1.isSelected()) {
            status = 1;
        } else if (jRadioButton2.isSelected()) {
            status = 0;
        } else {
            status = 0;
        }
        Date startDate = jDateChooser1.getDate();
        Date endDate = jDateChooser2.getDate();
        DeploymentInfoController deploymentInfoController = new DeploymentInfoController();
        boolean result = deploymentInfoController.update(deploymentInfo.getId(), name, description, status, startDate, endDate);
        if (result) {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin quá trình triển khai thành công");
            List<DeploymentInfo> deploymentInfos = deploymentInfoController.search("", "", null, null, null);
            this.form.setListDeploymentInfo(deploymentInfos);
            this.form.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin quá trình triển khai thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

