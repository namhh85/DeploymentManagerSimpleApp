    public void setData(User user, DeploymentInfoForm form, DeploymentInfo deploymentInfo) {
        this.deploymentInfo = deploymentInfo;
        this.user = user;
        this.form = form;
        
        jTextField1.setText(deploymentInfo.getName());
        jTextArea1.setText(deploymentInfo.getDescription());
        int status = deploymentInfo.getStatus();
        if (status == 0) {
            jRadioButton2.setSelected(true);
        } else if (status == 1) {
            jRadioButton1.setSelected(true);
        }
        jDateChooser1.setDate(deploymentInfo.getStartTime());
        jDateChooser2.setDate(deploymentInfo.getEndTime());
        jTextField2.setText(Utils.formatDate(deploymentInfo.getCreatedAt()));
    }
