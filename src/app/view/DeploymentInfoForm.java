    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (deployName == null) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một thông tin triển khai để sửa");
            return;
        }
        Optional<DeploymentInfo> opt = this.deploymentInfos.stream().filter(deploy -> deployName.equals(deploy.getName())).findFirst();
        if (!opt.isPresent()) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một thông tin triển khai để sửa");
            return;
        }
        EditDeploymentInfoForm editDeploymentInfoForm = new EditDeploymentInfoForm();
        editDeploymentInfoForm.setData(user, this, opt.get());
        editDeploymentInfoForm.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

