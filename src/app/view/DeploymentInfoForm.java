    public void setListDeploymentInfo(List<DeploymentInfo> deploymentInfos) {
        this.deploymentInfos = deploymentInfos;
        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        modelTable.setRowCount(0);
        int stt = 1;
        for (DeploymentInfo deploymentInfo : deploymentInfos) {
            String[] row = new String[] {
                String.valueOf(stt),
                deploymentInfo.getName(),
                deploymentInfo.getDescription(), 
                deploymentInfo.getUser().getName(), 
                deploymentInfo.getStatus() == 0 ? "Chưa hoàn thành" : "Hoàn thành", 
                String.valueOf(deploymentInfo.getStartTime()),
                String.valueOf(deploymentInfo.getEndTime()),
                Utils.formatDate(deploymentInfo.getCreatedAt()),
            };
            stt++;
            modelTable.addRow(row);
        }
    }
