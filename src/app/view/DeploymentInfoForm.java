    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (deployName == null) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một thông tin triển khai để xóa");
            return;
        }
        Optional<DeploymentInfo> opt = this.deploymentInfos.stream().filter(deploy -> deployName.equals(deploy.getName())).findFirst();
        if (!opt.isPresent()) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một thông tin triển khai để xóa");
            return;
        }
        DeploymentInfo deploymentInfo = opt.get();
        int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa thông tin triển khai này không?");
        if (confirm == 0) {
            DeploymentInfoController deploymentInfoController = new DeploymentInfoController();
            boolean result = deploymentInfoController.delete(deploymentInfo.getId());
            if (result) {
                JOptionPane.showMessageDialog(rootPane, "Xóa thông tin triển khai thành công");
                List<DeploymentInfo> deploymentInfos = deploymentInfoController.search("", "", null, null, null);
                setListDeploymentInfo(deploymentInfos);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Xóa thông tin triển khai thất bại");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

