    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (stepId == 0) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một bước để chỉnh sửa");
            return;
        }
        Optional<Step> opt = stepList.stream().filter(s -> s.getId() == stepId).findFirst();
        if (!opt.isPresent()) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một bước để chỉnh sửa");
            return;
        }
        Step step = opt.get();
        EditStepForm editStepForm = new EditStepForm();
        editStepForm.setData(step, this, deploymentInfo, stage);
        editStepForm.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (stepId == 0) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một bước để xóa");
            return;
        }
        Optional<Step> opt = stepList.stream().filter(s -> s.getId() == stepId).findFirst();
        if (!opt.isPresent()) {
            JOptionPane.showMessageDialog(rootPane, "Cần chọn một bước để xóa");
            return;
        }
        Step step = opt.get();
        int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa bước này không?");
        if (confirm == 0) {
            StepController stepController = new StepController();
            boolean result = stepController.delete(step);
            if (result) {
                JOptionPane.showMessageDialog(rootPane, "Xóa bước thành công");
                List<Step> stepList = stepController.findAll(deploymentInfo, stage);
                setStepList(stepList);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Xóa bước thất bại");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

