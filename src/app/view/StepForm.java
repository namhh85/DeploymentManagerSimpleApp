    public void setStepList(List<Step> stepList) {
        this.stepList = stepList;
        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        modelTable.setRowCount(0);
        int stt = 1;
        for (Step step : stepList) {
            List<Tool> toolList = step.getToolList();
            String toolListName = toolList.size() == 0 || toolList.get(0).getId() == 0 ? "" : toolList.stream().map(Tool::getName).collect(Collectors.joining(","));
            String[] row = new String[] {
                String.valueOf(stt),
                "STEP" + step.getId(),
                step.getName(),
                step.getDescription(),
                toolListName
            };
            stt++;
            modelTable.addRow(row);
        }
    }
